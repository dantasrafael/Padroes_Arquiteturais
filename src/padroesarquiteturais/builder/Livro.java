package padroesarquiteturais.builder;

public class Livro {

	private String introducao;
	private String indice;
	private String prefacio;
	private String capitulos;
	private String glossario;
	
	public String getIntroducao() {
		return introducao;
	}
	
	public void setIntroducao(String introducao) {
		this.introducao = introducao;
	}
	
	public String getIndice() {
		return indice;
	}
	
	public void setIndice(String indice) {
		this.indice = indice;
	}
	
	public String getPrefacio() {
		return prefacio;
	}
	
	public void setPrefacio(String prefacio) {
		this.prefacio = prefacio;
	}
	
	public String getCapitulos() {
		return capitulos;
	}
	
	public void setCapitulos(String capitulos) {
		this.capitulos = capitulos;
	}
	
	public String getGlossario() {
		return glossario;
	}
	
	public void setGlossario(String glossario) {
		this.glossario = glossario;
	}

}
